#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*c;

	c = (char *)s;
	i = 0;
	if (!c)
		return ;
	while (i < n)
	{
		c[i] = 0;
		i++;
	}
}
